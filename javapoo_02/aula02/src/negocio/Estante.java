package negocio;

public class Estante {
	
	// Propiedade da Classe 
		private int numero = 0;

	// Metodo construtor de classe 
		
	public Estante() {
		super();
	}

	public Estante(int numero) {
		super();
		this.numero = numero;
	}
	
	// Metodos de Acesso da Classe 
	
	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}
	
	
		
		
	
	
		

}
